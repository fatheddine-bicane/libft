#include <stddef.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t destsize)
{
	unsigned int	i;
	int				j;

	i = 0;
	while (src[i] != 0 && i < destsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	j = i;
	dst[j] = 0;
	if (src[i] != 0)
	{
		while (src[i] != 0)
			i++;
	}
	return (i);
}
