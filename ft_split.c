#include "libfy.h"

char	**ft_split(char const *s, char c);
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (s[i] != 0)
	{
		while (s[i] == c)
			i++;
		if (s[i] != c)
		{
			count++;
			while (s[i] != c)
				i++;
		}
		i++;
	}
}
