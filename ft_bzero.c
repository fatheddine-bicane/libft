#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*s_tc;
	size_t			i;

	s_tc = ((char *)(s));
	i = 0;
	while (i < n)
	{
		s_tc[i] = 0;
		i++;
	}
}
